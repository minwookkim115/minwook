a = [1, 2, 3]

# 슬라이싱
b = a[:]
b[0] = 100
print(a, b) # [1, 2, 3] [100, 2, 3]

# copy
c = a.copy()
c[0] = 100
print(a, c) # [1, 2, 3] [100, 2, 3]

# 얕은 복사의 한계
a = [1, 2, [1, 2]]
b = a[:]
b[2][0] = 999
print(a, b) # [1, 2, [999, 2]] [1, 2, [999, 2]]

a = [1, 2, [1, 2]]
c = a.copy()
c[2][0] = 999
print(a, c) # [1, 2, [999, 2]] [1, 2, [999, 2]]

# [1, 2, [1, 2]] 는 복사가 됨
# 2번째 [1, 2]는 복사가 안되고 같은 값을 보고있음